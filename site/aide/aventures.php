<h2>Une aventure ?</h2>
<p class="alinea">
Un aventure dont vous etes le heros est une histoire, ou, a chaque page, vous decidez la suite, quelle sera la destinee du heros. Cette partie traitera de comment y jouer. Pour savoir en creer en toute facilitee, une fois que vous maitrisez le jeu, allez sur l'onglet de la barre d'aide "Creation".</p>
<p class="alinea">
La liste de toutes les avantures actives est dans la page "Aventures". Chaque aventure, dans un cadre rouge, est accompagnee d'un court resume, le synopsis. Vous n'avez qu'a cliquez sur le nom d'une aventure pour y acceder. Une fois sur la page, vous avez access a l'introduction, essentielle pour entrer dans l'univers de l'aventure. Vous avez aussi, en dessous, un module de vote et des bouttons de partage</p>
<p class="alinea">
ATTENTION: si vous jouez a une aventure sans vous inscrire, elle est enregistree dans votre navigateur web (pas des cookies). Ainsi, selon sa configuration, l'aventure pourait ne pas s'enregistrer corectement, ou disparaitre du jour au lendemain. Avoir un compte sur le site vous evite ces desagrements</p>

<h2>Commencer a jouer</h2>
<p class="alinea">
La page ou vous arrivez apres avoir selectionne une aventure est la page d'introduction. Le lien du menu, juste au dessus, vous permet d'y retourner quand vous voullez. Les bouttons du haut vous permettent de (re)comencer/continuer votre partie sur cette aventure. Cliquez alors sur comencer/continuer(ou recomencer si vous voulez vraiment tout recomencer)</p>
<p class="alinea">
Apres avoir clique sur le boutton, vous etes sur une des multiples page de l'aventure. Une aventure n'est en fait qu'un ensemble de pages reliees entres elles. Le contenu se decompose en trois partie:
<ul>
    <li>Le boutton pour acceder a vos notes</li>
    <li>Le texte de la page</li>
    <li>Qu se passe-t-il apres</li>
</ul>
</p>
<p class="alinea">
Le bouttons vous permet de prendre des nots durant l'aventure, sur ce qui s'est passe, etc... Elles sont conservee si vous recomencez l'aventure.</p>
<p class="alinea">
Le texte est l'element majeur d'une page. Il decrit ce qui se passe, ce que votre heros, vous, vivez. Il peut etre accompagne d'images, citations...</p>
<p class="alinea">
Enfin, la derniere partie indique la ou les suites possibles. Cela peut etre une liste de choix auquels vous etes confrontes, comme une phrase vous indiquant que vous avez (enfin!) termine l'aventure(gagne, en quelque sorte)... ou que vous avez perdu!</p>

<h2>A vous de jouer</h2>
<p class="alinea">
Vous etes maintenant pres a affronter les perils de ces aventures! Armez-vouz de votre courage, et que les vents vous soient favorables!</p>

<h2>Des erreurs?</h2>
<p class="alinea">
Si il est affiche qu'une page n'existe pas, c'est qu'elle a etee supprimee par le createur de l'aventure. Si il est indique qu'une page n'a pas encore etee modifiee, dans un cadre rouge, c'est que cette page n'a aucun contenu, et ne peut donc pas etres affichee. Dans tous ces cas, il est conseille de contacter le createur de l'aventure pour lui indiquer le probleme, en precisant de quelle page vous proveniez.</p>
